'''
1. k칸 앞으로 점프 --> k만큼 건전지 소모
2. (현재까지 온 거리 * 2) 순간이동 --> 소모 x
3. 건전지 사용량 최소로 이동해야 함

* n에서 1로 가는 경우를 생각해보자 (거꾸로!)
'''

def solution(n):
    cnt = 1 # 0에서 1로 갈때는 무조건 점프!
    
    while True:
        if n == 1:
            break
        
        # 현재 위치가 짝수인 경우에는 순간이동
        if n % 2 == 0:
            n //= 2
        # 홀수인 경우 점프하고, 소모량 +1
        else:
            n -= 1
            cnt += 1
    
    return cnt